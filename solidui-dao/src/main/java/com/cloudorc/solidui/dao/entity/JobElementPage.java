package com.cloudorc.solidui.dao.entity;

import com.baomidou.mybatisplus.annotation.IdType;
import com.baomidou.mybatisplus.annotation.TableId;
import com.baomidou.mybatisplus.annotation.TableName;

import java.util.Date;

/**
 * store between page and element
 */
@TableName("solidui_job_element_page")
public class JobElementPage {

    @TableId(value = "id", type = IdType.AUTO)
    private Long id;

    private Long jobPageId;

    private Long jobElementId;

    private String position;

    private Date createTime;

    private Date updateTime;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getJobPageId() {
        return jobPageId;
    }

    public void setJobPageId(Long jobPageId) {
        this.jobPageId = jobPageId;
    }

    public Long getJobElementId() {
        return jobElementId;
    }

    public void setJobElementId(Long jobElementId) {
        this.jobElementId = jobElementId;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}
